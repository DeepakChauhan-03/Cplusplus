//WAP to read five numbers and print average of all values
#include<iostream>
using namespace std;
int main(){
    int n1,n2,n3,n4,n5,average;
    n1=4;
    n2=6;
    n3=8;
    n4=10;
    n5=12;
    average = (n1+n2+n3+n4+n5)/5;
    cout<<"Average of given five numbers is: "<<average;
    return 0;
}