//Size of operator
#include<iostream>
using namespace std;
int main(){
    int i;
    float f;
    double d;
    char c;
    cout<<"Size of int variable: "<<sizeof(i)<<endl;
    cout<<"Size of float variable: "<<sizeof(f)<<endl;
    cout<<"Size of double variable: "<<sizeof(d)<<endl;
    cout<<"Size of char variable: "<<sizeof(c)<<endl;
    return 0;
}
