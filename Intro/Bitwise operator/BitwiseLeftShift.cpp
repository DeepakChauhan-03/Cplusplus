//Bitwise Left Shift Operator
#include<iostream>
using namespace std;
int main(){
    int x,y;
    x=4;
    y=x<<1;
    cout<<"Result of Left Shift operator is: "<<y<<endl;
    cout<<"Result of Left Shift operator is: "<<(x<<2)<<endl;
    cout<<"Result of Left Shift operator is: "<<(x<<3)<<endl;
    return 0;
}
//Value will be doubled